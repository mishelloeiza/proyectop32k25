    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
    try {
        // Crear la ventana
        JFrame ventana = new JFrame("Lista de Usuarios");
        ventana.setSize(500, 350);
        ventana.setLocationRelativeTo(null);
        ventana.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);

        // Modelo de la tabla
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID");
        modelo.addColumn("Nombre");
        modelo.addColumn("Perfil"); 

        JTable tabla = new JTable(modelo);
        JScrollPane scroll = new JScrollPane(tabla);
        ventana.add(scroll, BorderLayout.CENTER);

        // ? Conectar directamente a la base de datos y ejecutar la consulta SQL
        Connection conn = Conexion.getConnection();
        String sql = "SELECT u.id_usuario, u.username, COALESCE(r.id_perfil, 'Sin perfil') AS perfil " +
                     "FROM usuario u " +
                     "LEFT JOIN relperfusu r ON u.id_usuario = r.id_usuario";
        PreparedStatement stmt = conn.prepareStatement(sql);
        ResultSet rs = stmt.executeQuery();

        while (rs.next()) {
            modelo.addRow(new Object[]{rs.getInt("id_usuario"), rs.getString("username"), rs.getString("perfil")});
        }

        // Cerrar conexi√≥n y mostrar ventana
        rs.close();
        stmt.close();
        conn.close();

        ventana.setVisible(true);
    } catch (Exception ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage());
    }

    }//GEN-LAST:event_jButton3ActionPerformed

