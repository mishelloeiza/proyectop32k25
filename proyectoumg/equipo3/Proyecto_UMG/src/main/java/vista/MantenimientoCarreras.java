    public void llenadoDeCombos() {
        CarreraDAO carreraDAO = new CarreraDAO();
        List<Carrera> carreras = carreraDAO.select();
        cbox_empleado.addItem("Seleccione una opci√≥n");
        for (int i = 0; i < carreras.size(); i++) {
            cbox_empleado.addItem(carreras.get(i).getNombreCarrera());
        }
    }

    public void llenadoDeTablas() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Codigo Carrera");
        modelo.addColumn("Nombre Carrera");
        modelo.addColumn("Codigo Facultad");
        modelo.addColumn("Estatus Carrera");
        CarreraDAO carreraDAO = new CarreraDAO();
        List<Carrera> carreras = carreraDAO.select();
        tablaCarreras.setModel(modelo);
        String[] dato = new String[4];
        for (int i = 0; i < carreras.size(); i++) {
            dato[0] = Integer.toString(carreras.get(i).getCodigoCarrera());
            dato[1] = carreras.get(i).getNombreCarrera();
            dato[2] = Integer.toString(carreras.get(i).getCodigoFacultad());
            dato[3] = carreras.get(i).getEstatusCarrera();
            //System.out.println("vendedor:" + vendedores);
            modelo.addRow(dato);
        }
    }

    public void buscarCarrera() {
        Carrera carreraAConsultar = new Carrera();
        CarreraDAO carreraDAO = new CarreraDAO();
        carreraAConsultar.setCodigoCarrera(Integer.parseInt(txtbuscado.getText()));
        carreraAConsultar = carreraDAO.query(carreraAConsultar);
        txtNombre.setText(carreraAConsultar.getNombreCarrera());
        txtCodigoF.setText(Integer.toString(carreraAConsultar.getCodigoCarrera()));
        txtEstatus.setText(carreraAConsultar.getEstatusCarrera());
    }

